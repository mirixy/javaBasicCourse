public class Variablen {
    public static void main(String[] args){
        int meinAlter = 33; //Initialisierung einer Variable
        System.out.println("My Age is: " + meinAlter);
        int meinAlter2; //Deklaration einer Variable
        meinAlter2 = 33; //Zuweisung
        meinAlter = 34; //Zuweisung
        System.out.println("My new age is: " + meinAlter);
    }
}
